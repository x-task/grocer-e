    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Post  $post
     * @return mixed
     */
    public function view(User $user, Post $post)
    {
        // If you want only the authorized user to see this post
        return $user->id == $post->user_id;
    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @return mixed
     */
    public function create(User $user)
    {
        return $user->is($user);    // Checks if the authenticated user is creating this post
        /* This is reflected in the PostController.php methods
        with the use of authorize('create', Model::class) helper function */
    }

    /**
     * Determine whether the user can update the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Post  $post
     * @return mixed
     */


    public function update(User $user, Post $post)
    {
        // We want only the user to update this post
        return $user->id == $post->user_id;
        /* So we chech to see if the user ID in users table is
        equal to user_id column of posts table */
    }

